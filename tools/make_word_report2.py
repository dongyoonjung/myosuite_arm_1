"""쉬운 언어 Word 보고서 — track 데이터 생성·결과 / full-seq·손목 여부 / reward(필수).
.venv/bin/python tools/make_word_report2.py"""
import os
import sys

from docx import Document
from docx.shared import Inches, Pt
from docx.enum.text import WD_ALIGN_PARAGRAPH

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
FIG = os.path.join(ROOT, "results", "report")


def H(d, t, l=1): d.add_heading(t, level=l)
def P(d, t, s=10.5):
    p = d.add_paragraph(); r = p.add_run(t); r.font.size = Pt(s); return p


def tbl(d, headers, rows):
    t = d.add_table(rows=1, cols=len(headers)); t.style = "Light Grid Accent 1"
    for i, h in enumerate(headers):
        c = t.rows[0].cells[i]; c.text = ""; rn = c.paragraphs[0].add_run(h); rn.bold = True; rn.font.size = Pt(9.5)
    for row in rows:
        cs = t.add_row().cells
        for i, v in enumerate(row):
            cs[i].text = ""; rn = cs[i].paragraphs[0].add_run(str(v)); rn.font.size = Pt(9.5)


def img(d, name, w=6.4, cap=None):
    p = os.path.join(FIG, name)
    if os.path.exists(p):
        d.add_picture(p, width=Inches(w)); d.paragraphs[-1].alignment = WD_ALIGN_PARAGRAPH.CENTER
        if cap:
            c = d.add_paragraph(); c.alignment = WD_ALIGN_PARAGRAPH.CENTER
            r = c.add_run(cap); r.italic = True; r.font.size = Pt(9)


doc = Document()
doc.add_heading("가상 팔에서 '움직임만 보고' 약한 근육 찾기 — 결과 보고서", 0)
s = doc.add_paragraph(); s.alignment = WD_ALIGN_PARAGRAPH.CENTER
r = s.add_run("쉬운 설명 버전 · 2026-06-11"); r.italic = True; r.font.size = Pt(11)

H(doc, "1. 한눈에 — 무엇을 했나", 1)
P(doc, "컴퓨터 속 가상의 팔(근육 모형)에서 특정 근육을 일부러 약하게 만든 뒤, 그 팔이 움직이는 "
  "모습(관절 각도의 시간 변화 = 운동학)만 보고 \"어느 근육이 얼마나 약해졌는지\"를 자동으로 "
  "맞히는 인공지능(회귀 모형)을 만들었습니다. 근육의 전기신호(EMG)는 쓰지 않고, 오직 움직임만 "
  "씁니다(실제 환자에서 모션캡처로 얻는 정보와 같음).")
P(doc, "이를 위해 가상 팔에게 '사람다운 거상(팔 들어올리기) 동작'을 따라 하도록 강화학습으로 가르치고, "
  "근육을 약화시킨 여러 경우를 대량으로 시뮬레이션해 데이터를 모은 뒤, 그 데이터로 회귀 모형을 "
  "학습시켰습니다.")

# ── 2. track(따라 할 목표) 데이터 생성 ──
H(doc, "2. 따라 할 목표 동작(track 데이터)을 어떻게 만들었나", 1)
P(doc, "가상 팔이 '따라 할 목표 궤적'이 필요합니다. 이 목표는 실제 사람 데이터(KIMHu: 20명이 팔을 "
  "들어올린 모션캡처)에서 가져와 다양하게 변형해 만듭니다. 한 가지 동작만 쓰면 단조로우니, 사람마다 "
  "다른 속도·높이·모양을 반영해 '다양하고 현실적인' 목표들을 생성해야 합니다.")
P(doc, "생성 방법을 세 번 발전시켰습니다:")
tbl(doc, ["방법", "설명", "한계/장점"], [
    ["① warp (평균+1모드)", "사람 평균 동작 + 1개 변형축을 늘려/줄여 만듦", "변형이 단조로움(모양 1가지)"],
    ["② VAE (딥러닝 생성모델)", "사람 동작들을 신경망으로 학습해 새 동작 생성", "다양하나 분포 폭이 실제보다 좁음(평균화)"],
    ["③ fPCA / ProMP (최종)", "사람 동작의 주요 변형 패턴을 통계로 뽑아 그 분포에서 샘플", "실제 분포를 가장 충실히 재현 ✓"],
])
P(doc, "결과: 최종 채택한 fPCA가 가장 좋았습니다. 생성한 동작의 '정점 높이' 분포 폭이 실제와 거의 같고"
  "(실제 94±9°, fPCA 95±9°; VAE는 95±5°로 너무 좁음), 모양 분포도 실제와 잘 맞으며(통계 거리 가장 작음), "
  "베끼지 않고 다양하게 생성합니다. 즉 'KIMHu를 다양하게 변형해 현실적인 목표 동작을 만든다'는 목적을 "
  "가장 잘 달성합니다.")
img(doc, "fig9_refgen_compare.png", 6.6, "그림 1. 생성된 거상 곡선(평균±폭): 실제(회색)·VAE(주황)·fPCA(파랑). "
    "fPCA의 폭이 실제와 일치(VAE는 좁음).")

# ── 3. reward function (필수) ──
H(doc, "3. 보상 함수(reward) — 팔에게 '잘했다/못했다'를 알려주는 점수", 1)
P(doc, "강화학습에서 가상 팔(정책)은 매 순간 받는 '점수(보상)'를 최대화하도록 시행착오로 배웁니다. "
  "이 점수를 어떻게 설계했는지가 핵심입니다. 세 부분의 곱·합으로 구성됩니다:")
P(doc, "  보상 = (목표 따라가기) × (움직임 매끄러움) + (힘 아끼기) + (벌점)", 11)
H(doc, "① 목표 따라가기 (TASK) — 핵심", 2)
P(doc, "각 관절이 목표 각도에 얼마나 가까운지 점수화합니다. 수식:")
P(doc, "  TASK = Σ wᵢ · exp(−kᵢ · [max(0, |현재각−목표각| − 밴드)]²)", 10.5)
P(doc, "여기서 핵심은 '느슨한 허용 밴드(soft band, 약 10~15°)'입니다. 목표에서 밴드 이내면 벌점이 "
  "전혀 없고(사람마다 조금씩 다른 건 허용), 밴드를 벗어나면 점수가 급격히 떨어집니다.")
P(doc, "왜 느슨하게? 만약 빡빡하게 정확히 따라 하게 만들면, 근육이 약해도 다른 근육으로 억지로 "
  "목표를 맞춰버려 '약화'가 움직임에 드러나지 않습니다. 느슨하게 둬야 '심하게 약하면 목표에 "
  "못 미친다(이탈)'가 신호로 나타납니다. 이것이 이 연구의 핵심 원리입니다.")
H(doc, "② 움직임 매끄러움 (QUALITY)", 2)
P(doc, "  QUALITY = exp(−(흔들림 + 명령변화 + 정지))")
P(doc, "떨림·급격한 변화 없이 부드럽게 움직이고, 동작 끝에서 잘 멈추도록 유도합니다. (사람다운 "
  "매끄러운 동작을 만드는 부분.)")
H(doc, "③ 힘 아끼기 (EFFORT) + 벌점", 2)
P(doc, "  EFFORT = − (근육 활성의 평균)")
P(doc, "근육을 불필요하게 세게 쓰면 감점 → 자연스러운 '최소 노력' 동작을 배웁니다. 그 밖에 목표에서 "
  "너무 크게 벗어나면(예: 50° 이상) 에피소드를 종료하는 벌점이 있습니다(단, 약화로 인한 적당한 "
  "이탈은 살아남도록 밴드를 넓게 둠).")
P(doc, "정리: \"목표를 느슨하게 따라가되, 부드럽게, 힘을 아끼게\" 가르칩니다. 이 설계 덕분에 경증 약화는 "
  "다른 근육 보상으로, 중증 약화는 움직임 이탈로 자연히 갈라져 식별이 가능해집니다.")

# ── 4. full sequence 여부 ──
H(doc, "4. '전체 동작(full sequence)'을 쓸까, '들어올리기(rise)만' 쓸까?", 1)
P(doc, "KIMHu 동작은 [팔 들어올리기(rise) → 팔꿈치 굽혀 머리로 → 손목 조작 → 내리기]의 긴 전체 "
  "동작입니다. 이 중 '들어올리기'만 쓸지, 전체를 쓸지를 비교했습니다.")
tbl(doc, ["사용 구간", "어깨/팔꿈치 식별 R²", "설명"], [
    ["들어올리기(rise)만", "0.81 (높음)", "근육이 중력에 맞서 최대로 일하는 구간 = 약화가 가장 잘 드러남"],
    ["전체 동작(full sequence)", "0.55~0.60 (낮아짐)", "정보가 적은 손목조작·복귀 구간이 섞여 신호가 희석됨"],
])
P(doc, "발견: '들어올리기' 구간이 어깨 근육 식별에 가장 좋습니다. 팔을 들어 올릴 때 어깨 근육이 중력에 "
  "맞서 최대로 힘을 쓰므로, 그 근육이 약하면 또렷이 드러납니다. 반면 전체 동작은 정보가 적은 구간"
  "(손목 만지작거리기, 팔 내리기)이 섞여 어깨 신호가 묽어집니다(R² 0.81 → 0.55~0.60).")

# ── 5. 손목 여부 ──
H(doc, "5. 손목을 '잠글까', '풀까'?", 1)
P(doc, "기본 설정은 손목을 고정(잠금)하고 어깨·팔꿈치에 집중합니다(손목은 원래 식별 대상이 아니고, "
  "모션캡처가 손목을 잘 못 잡음). 사용자 요청으로 손목을 풀어(잠금 해제) 손목 근육 6개를 추가하고 "
  "전체 동작을 쓰는 실험도 했습니다.")
tbl(doc, ["설정", "어깨/팔꿈치", "손목 근육"], [
    ["손목 잠금 + rise만", "0.81 (최고)", "— (식별 안 함)"],
    ["손목 풀기 + 전체동작, T1", "0.60", "0.81 (식별됨!)"],
    ["손목 풀기 + 전체동작, T1+T2", "0.55", "0.74 (식별됨)"],
])
P(doc, "발견: 손목을 풀고 전체 동작을 쓰면 손목 근육의 약화도 움직임만으로 잘 식별됩니다(R² ~0.75~0.81). "
  "하지만 그 대가로 어깨·팔꿈치 식별이 떨어집니다(0.81 → 0.55~0.60). 과제를 둘(T1+T2)로 늘려도 "
  "어깨/팔꿈치는 회복되지 않았습니다 — 즉 저하의 원인은 '한 과제만 써서'가 아니라 '전체 동작·손목 "
  "해제 자체'입니다(들어올리기라는 핵심 정보 구간이 희석되기 때문).")
img(doc, "fig11_compare_setups.png", 6.8, "그림 2. 세 설정 비교. 회색=손목잠금+rise(어깨/팔꿈치 최고), "
    "주황/파랑=손목풀기+전체동작(손목근 식별 추가, 어깨/팔꿈치는 저하). 오른쪽 음영=손목 근육.")

# ── 6. 결론 ──
H(doc, "6. 결론 — 쉬운 요약", 1)
P(doc, "• 목표 동작 생성: 사람 데이터(KIMHu)를 통계 생성모델(fPCA)로 다양하고 현실적으로 변형하는 것이 "
  "가장 좋았습니다(딥러닝 VAE보다 분포를 충실히 재현).")
P(doc, "• 보상 함수: '느슨하게 따라가되 부드럽고 힘을 아끼게' 설계 → 약화가 '보상' 또는 '이탈'로 드러나 "
  "식별 가능.")
P(doc, "• 핵심 결과: 근육의 전기신호 없이 '움직임만'으로 어깨·팔꿈치 근육 약화를 강하게 식별(R²≈0.81, "
  "들어올리기 구간·손목 잠금).")
P(doc, "• 트레이드오프: 손목을 풀고 전체 동작을 쓰면 손목 근육까지 식별(~0.8)하지만 어깨/팔꿈치는 "
  "희생됩니다. 한 동작으로 모두를 동시에 최대 식별할 수는 없습니다.")
P(doc, "• 실용 지침: 어깨/팔꿈치가 목표면 '들어올리기 + 손목 잠금', 손목이 목표면 '전체 동작 + 손목 풀기'.")

out = os.path.join(ROOT, "보고서_쉬운설명_2026-06-11.docx")
doc.save(out)
print("saved:", out, "| 문단", len(doc.paragraphs), "표", len(doc.tables), "그림", len(doc.inline_shapes))
